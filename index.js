'use strict';

var api = require('api-toc');

/**
 * Generate a clickable list of API "code-links" for the methods in directory.
 * Each link takes you to the beginning line of code for the corresponding method.
 *
 * Usage:
 *
 * ```js
 * {%= apitoc('lib/') %}
 * ```
 */

module.exports = function(dir, append) {
  if (typeof dir !== 'string') {
    throw new TypeError('helper-codelinks expects the first argument to be a string.');
  }
  var res = api(dir, append);
  res += '\n\n';
  res += '_(Code links generated by Verb\'s [api-toc] helper)_';
  res += '\n\n';
  res += '[api-toc]: https://github.com/jonschlinkert/api-toc'
  return res;
};
